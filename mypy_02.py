# Numbers in Python 3
# Types of numbers: int, float, complex

# Integer
a = 496
print(type(a))
print('a =',a,'\n')

# Float
e = 2.718281828459045
print(type(e))
print('e =', e,'\n')

# Complex
z = 2 + 5j
print(type(z))
print('z =', z)
print('Re(z) =',z.real)
print('Im(z) =',z.imag)

